const express = require('express');
const passport = require('passport');
const { userSchema, loginSchema } = require('../utils/validationSchemas');
const validate = require('../middlewares/validators/zodValidator');
const authController = require('../controllers/userController');

const router = express.Router();

// @desc    Register user
// @route   POST /api/v1/auth/register
// @access  Public
router.post('/register', validate(userSchema), authController.register);

// @desc    Login user
// @route   POST /api/v1/auth/login
// @access  Public
router.post('/login', validate(loginSchema), 
  passport.authenticate('local', { session: false }),
  authController.login
);

// @desc    Get current logged in user
// @route   GET /api/v1/auth/me
// @access  Private
router.get('/me', authController.getMe);

// @desc    Update user details
// @route   PUT /api/v1/auth/updatedetails
// @access  Private
router.put('/updatedetails', authController.updateDetails);

// @desc    Update password
// @route   PUT /api/v1/auth/updatepassword
// @access  Private
router.put('/updatepassword', authController.updatePassword);

// @desc    Forgot password
// @route   POST /api/v1/auth/forgotpassword
// @access  Public
router.post('/forgotpassword', authController.forgotPassword);

// @desc    Reset password
// @route   PUT /api/v1/auth/resetpassword/:resettoken
// @access  Public
router.put('/resetpassword/:resettoken', authController.resetPassword);

// @desc    Reset password with OTP
// @route   PUT /api/v1/auth/resetpassword-otp
// @access  Public
router.put('/resetpassword-otp', authController.resetPasswordWithOTP);

// @desc    Verify OTP
// @route   POST /api/v1/auth/verify-otp
// @access  Public
router.post('/verify-otp', authController.verifyOTP);

// @desc    Logout user
// @route   GET /api/v1/auth/logout
// @access  Private
router.get('/logout', authController.logout);

module.exports = router;